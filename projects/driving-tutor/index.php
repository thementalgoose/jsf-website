          <p>Driving Tutor aims to be your all in one revision app for learning to drive in the UK. This was made as an A level project and I hope that some of you can take what i've done and use it to help you pass your driving test</p>
          <p>Driving Tutor contains:</p>
          <ul>
          <li>A fully featured theory test, including just over 200+* real theory questions for you to test your knowledge on</li>
          <li>10* hazard perception videos for you to practice your awareness</li>
          <li>Tutorials and helpful diagrams on how to do any one of the 5 manoeuvres (Parallel parking, Reversing round a corner, Turn in a road, Bay parking*, and the Emergency stop*)</li>
          <li>All of the possible Show-me Tell-me questions that could come up at the start of the test</li>
          <li>An example of all the types of road signs you may encounter on British roads.</li>
          <li>Some driving situations information and diagrams, including the roundabouts and rights of way, to give you some explanation on certain road situations</li>
